    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Usuario usuarioAct = (Usuario)AppState.listausuarios.find(UsuarioTxt.getText());
        int selectedIndex = categoriaList.getSelectedIndex();
        if (rutaImagenTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una Imagen", "IMAGEN", JOptionPane.WARNING_MESSAGE);
        } else if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una categoría.", "CATEGORÍA", JOptionPane.WARNING_MESSAGE);
        } else {
            File archivo = new File(rutaImagenTxt.getText());
            String fileName=archivo.getName();

            String nombre = FilenameUtils.getBaseName(fileName);
            ImageIcon imagen = new ImageIcon(rutaImagenTxt.getText());
            Imagen nuevaImagen = new Imagen();
            nuevaImagen.setImageIcon(imagen);
            nuevaImagen.setNombre(nombre);
            nuevaImagen.setRutaImagen(rutaImagenTxt.getText());

            Categoria categoria = (Categoria)usuarioAct.getListaCategorias().get(categoriaList.getSelectedIndex());
            categoria.agregarImagen(nuevaImagen);

            JOptionPane.showMessageDialog(null, "Imagen agregada correctamente a la categoría: "+categoriaList.getSelectedValue(), "Ugallery", JOptionPane.INFORMATION_MESSAGE); 
            categoria.ImprimirImagenes();
            System.out.println("----------------------------------------------");

            rutaImagenTxt.setText(null);
            nombreImagenTxt.setText(null);
            imagenLbl.setIcon(null);
        }
        
        
        
       
    }//GEN-LAST:event_jButton4ActionPerformed

