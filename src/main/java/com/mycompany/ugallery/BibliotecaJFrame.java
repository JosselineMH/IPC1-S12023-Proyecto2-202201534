    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        String nombreUs = UsuarioTxt.getText();
        Usuario usuarioActual = (Usuario) AppState.listausuarios.find(nombreUs);

        // Validar que se haya seleccionado una categoría
        int index = categoriaList.getSelectedIndex();
        if (index >= 0) {
            String nombreCat = categoriaList.getModel().getElementAt(index);
            Categoria categoriaSeleccionada = usuarioActual.buscarCategoria(nombreCat);
            if (categoriaSeleccionada == null) {
                JOptionPane.showMessageDialog(null, "La categoría no existe para este usuario", "CATEGORÍA NO ENCONTRADA", JOptionPane.WARNING_MESSAGE);
            } else {
                // Eliminar todas las imágenes de la categoría
                usuarioActual.eliminarCategoria(categoriaSeleccionada);
                ListaDobleImagen listaImagenes = categoriaSeleccionada.getListaimagenes();
                listaImagenes.clear();

                // Actualizar el JList de categorías
                ArrayList<Categoria> categorias = usuarioActual.getListaCategorias();
                if (!categorias.isEmpty()) {
                    DefaultListModel<String> model = new DefaultListModel<>();
                    for (Categoria categoria : categorias) {
                        model.addElement(categoria.getNombreCategoria());
                    }
                    categoriaList.setModel(model);
                    categoriaList.setSelectedIndex(0); // Seleccionar la primera categoría de la lista
                } else {
                    DefaultListModel<String> model = new DefaultListModel<>();
                    categoriaList.setModel(model);
                }
                
                JOptionPane.showMessageDialog(null, "Categoría eliminada correctamente", "CATEGORÍA", JOptionPane.INFORMATION_MESSAGE);
                 imagenLbl.setIcon(null);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una categoría", "CATEGORÍA NO SELECCIONADA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton9ActionPerformed

