    private void categoriaListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_categoriaListMouseClicked
        Usuario usuarioActual = (Usuario) AppState.listausuarios.find(UsuarioTxt.getText()); 
        Categoria categoria = (Categoria)usuarioActual.listacategoria.get(categoriaList.getSelectedIndex());
        String NombreImagen;
        if(categoria!=null) {
            ListaDobleImagen listaImagenes = categoria.getListaimagenes();
            if (listaImagenes.get(0)!=null) {
                Imagen iconImagen = (Imagen) listaImagenes.get(0);
                NombreImagen =iconImagen.nombre;
                ImageIcon iconoImagen = iconImagen.getImageIcon();
                Image Imagen = iconoImagen.getImage();
                Imagen = Imagen.getScaledInstance(imagenLbl.getWidth(), imagenLbl.getHeight(), Image.SCALE_DEFAULT);
                imagenLbl.setIcon(new ImageIcon(Imagen));
                nombreImagenTxt.setText(NombreImagen);
                contadorLbl.setText("1");
                contadortotal.setText(String.valueOf(listaImagenes.getSize()));
                
                
            }else{
                
                contadorLbl.setText("0");
                contadortotal.setText("0");
                nombreImagenTxt.setText(null);
                imagenLbl.setIcon(null); 
            }
        }
    }//GEN-LAST:event_categoriaListMouseClicked

