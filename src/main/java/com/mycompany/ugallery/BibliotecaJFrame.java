    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

       String nombreCat = nombreCategoriaTxt.getText();
       Usuario usuarioActual = (Usuario) AppState.listausuarios.find(UsuarioTxt.getText());
       boolean categoriaExiste = false;

       Categoria addCategoria = new Categoria();


        // Validar que se haya ingresado un nombre de categoría
        if (nombreCat.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el nombre de la categoría", "CAMPO VACÍO", JOptionPane.WARNING_MESSAGE);
        } else {
            for (Categoria categoria : usuarioActual.getListaCategorias()) {
                if (categoria.getNombreCategoria().equals(nombreCat)) {
                    categoriaExiste = true;
                    break;
                }
            }

            if (categoriaExiste) {
                JOptionPane.showMessageDialog(null, "Ya existe una categoría con ese nombre para este usuario", "CATEGORÍA DUPLICADA", JOptionPane.WARNING_MESSAGE);
            } else {
                addCategoria.setNombreCategoria(nombreCat);
                usuarioActual.agregarCategoria(addCategoria);
                JOptionPane.showMessageDialog(null, "Categoría agregada correctamente", "CATEGORÍA", JOptionPane.INFORMATION_MESSAGE);
                DefaultListModel<String> model = null;
                if (categoriaList.getModel() == null) {
                    model = new DefaultListModel<String>();
                    categoriaList.setModel(model);
                } else {
                    model = (DefaultListModel<String>) categoriaList.getModel();
                }

                model.addElement(addCategoria.getNombreCategoria());
                nombreCategoriaTxt.setText(null);
            }
        } 
        
    }//GEN-LAST:event_jButton6ActionPerformed

